"""## **10. Plot Confusion Matrix**
The confusion matrix is a specialized tool for evaluating the performance of a classification model. It provides a detailed breakdown of how many times each tissue category was correctly identified and where the model made specific errors.

*   `plot_confusion_matrix(cm)` custom function designed to visualize the raw numbers of a confusion matrix in an intuitive, color-coded grid.
*   Annotating Cell Values:
    *   `itertools.product` iterates through every cell in the grid to place the exact count of images.
    *   `thresh = cm.max() / 2.0` calculates a threshold to automatically switch text color between white and black, ensuring the numbers are visible against both light and dark backgrounds.
*  `plt.xlabel("Predicted label") / plt.ylabel("True label")` organizes the chart so you can see what the tissue actually was (True) versus what the AI thought it was (Predicted).


"""

# 10 Plot Confusion Matrix

def plot_confusion_matrix(cm):
    plt.figure(figsize=(8, 8))
    plt.imshow(cm, cmap=plt.cm.Blues)
    plt.title("Confusion matrix")
    plt.xticks(range(num_classes), class_names)
    plt.yticks(range(num_classes), class_names)

    #Annotating cell values
    thresh = cm.max() / 2.0
    for i, j in itertools.product(range(cm.shape[0]), range(cm.shape[1])):
        plt.text(
            j, i, format(cm[i, j], "d"),
            horizontalalignment="center",
            color="white" if cm[i, j] > thresh else "black"
        )

    plt.tight_layout()
    plt.xlabel("Predicted label")
    plt.ylabel("True label")
    plt.show()

plot_confusion_matrix(cm)

"""## **11. Visualise Correctly and Incorrectly Classified Images**
This section performs a qualitative evaluation of the model by displaying specific examples from the test set. By comparing instances where the model succeeded versus where it failed, you can gain insights into the visual patterns the model has mastered and those it finds ambiguous.

*   Logic for Selection: the code iterates through the ds_test dataset and compares the true_l (ground truth) with the pred_l (model prediction). It collects the first three instances of each for comparison.
*   `tf.nn.softmax` converts the raw model outputs (logits) into a probability distribution. This allows the visualization to show the Prob (confidence score), indicating how sure the model was about its prediction.
*   Correctly Classified Examples: these serve as a baseline to confirm that the model correctly identifies the defining features of tissue types like Tumor or Stroma when they appear clearly.
*   Misclassified Examples: these images highlight the model's limitations. For example, if the model predicts "Muscle" for a "Stroma" image with a high probability score, it suggests a specific textural similarity that may require further data or model tuning to distinguish.


"""

# 11 Visualise Correctly and Incorrectly Classified Images

