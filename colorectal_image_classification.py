"""## **4. Image Preprocessing and Data Augmentation**
This section prepares the raw images for the neural network. Preprocessing ensures all data is in a uniform format, while augmentation helps the model generalize better by creating variations of the training data.


*   `preprocess(image, label)` custom function that prepares individual images.

    *   `tf.image.resize(..., (IMG_SIZE, IMG_SIZE))` standardizes all images to 160 × 160 pixels. This is necessary because neural networks require a fixed input shape.
    *   `image / 255.0` normalizes pixel values from the range [0, 255] to [0, 1]. This helps the model converge faster during training.
*   `map(preprocess, ...)` applies the preprocessing function to every image in the dataset across all CPU cores simultaneously using AUTOTUNE.
*   `batch(BATCH_SIZE)` groups images into sets of 32. Instead of looking at one image at a time, the model updates its weights based on the average error of the whole batch.
*   `prefetch(tf.data.AUTOTUNE)` allows the CPU to prepare the next batch of data while the GPU is still processing the current one. This eliminates data bottlenecks.
*   `data_augmentation` is to improve model generalisation and to artificially increase dataset variety. So the model can learn more vary features:
    *   `RandomFlip` flips images horizontally.
    *   `RandomRotation(0.15)` rotates images by up to 15%.
    *   `RandomZoom(0.1)` zooms in or out by up to 10%.
    *   `RandomTranslation` shifts the image slightly horizontally or vertically.
    *   `RandomContrast(0.1)` randomly adjusts the brightness contrast
"""

# 4 Image Preprocessing and Data Augmentation

IMG_SIZE = 160  # Resize all images to 160 by 160
BATCH_SIZE = 32 # Group images into 32 sets
AUTOTUNE = tf.data.AUTOTUNE

# Resize and rescale images
def preprocess(image, label):
    image = tf.image.resize(image, (IMG_SIZE, IMG_SIZE))
    image = image / 255.0     # Normalize pixel values into [0,1]
    return image, label

# Training dataset
ds_train = (
    ds_train
    .map(preprocess, num_parallel_calls=tf.data.AUTOTUNE)
    .shuffle(1000)
    .batch(BATCH_SIZE)
    .prefetch(tf.data.AUTOTUNE)
)

# Validation dataset
ds_val = (
    ds_val
    .map(preprocess, num_parallel_calls=tf.data.AUTOTUNE)
    .batch(BATCH_SIZE)
    .prefetch(tf.data.AUTOTUNE)
)

# Test dataset
ds_test = (
    ds_test
    .map(preprocess, num_parallel_calls=tf.data.AUTOTUNE)
    .batch(BATCH_SIZE)
    .prefetch(tf.data.AUTOTUNE)
)

# Data Augmentation
data_augmentation = keras.Sequential([
    layers.RandomFlip("horizontal"),
    layers.RandomRotation(0.15),
    layers.RandomZoom(0.1),
    layers.RandomTranslation(0.1, 0.1),
    layers.RandomContrast(0.1),
    ])

"""## **5. Build the CNN Model**

Simple CNN model architecture details and purpose of each layer:
*   `Convolutional Layers (Conv2D)` extract spatial features from input images, such as edges, textures, and patterns. Details:
    *   Block 1: 32 filters, 3×3 kernel, ReLU activation.
    *   Block 2: 64 filters, 3×3 kernel, ReLU activation.
    *   Block 3: 128 filters, 3×3 kernel, ReLU activation.
    *   Activation: ReLU introduces non-linearity, allowing the model to learn complex features.
*   `Batch Normalization (BatchNormalization)` normalizes activations for each mini-batch, improving training stability and convergence. Placed after each convolutional layer to reduce internal covariate shift.
*   `Pooling Layers (MaxPooling2D)` reduce spatial dimensions (height × width) while retaining important features. Helps decrease computational cost and provides spatial invariance.
    *   Pool size: 2×2 for each block.
*   `Flatten Layer (Flatten)` converts the 3D feature maps into a 1D vector, preparing it for fully connected layers.
*   `Fully Connected Layers (Dense)` combines the extracted features to perform high-level reasoning and classification.
    *   First Dense Layer: 128 units with ReLU activation.
*   `Dropout Layer` 40% dropout to prevent overfitting by randomly disabling neurons during training.
*   `Output Layer (Dense)` number of units equals num_classes (8) with Softmax activation.
    *   Softmax Activation: Converts the output logits into class probabilities for multi-class classification. Ensures all probabilities sum to 1 and can be interpreted as confidence scores for each class.



















"""

# 5 Build the CNN Model

model = keras.Sequential([
    # Input layer
    layers.Input(shape=(IMG_SIZE, IMG_SIZE, 3)),

    # Convolution Block 1
    layers.Conv2D(32, (3, 3), activation="relu", padding="same"),
    layers.MaxPooling2D((2, 2)),
    layers.BatchNormalization(),

    # Convolution Block 2
    layers.Conv2D(64, (3, 3), activation="relu", padding="same"),
    layers.MaxPooling2D((2, 2)),
    layers.BatchNormalization(),

    # Convolution Block 3
    layers.Conv2D(128, (3, 3), activation="relu", padding="same"),
    layers.MaxPooling2D((2, 2)),
    layers.BatchNormalization(),

    layers.GlobalAveragePooling2D(),
    layers.Flatten(), # Flatten 3D feature maps to 1D vector

    # Dense Layer 1
    layers.Dense(128, activation="relu"), # Fully connected layer for high-level features
    layers.Dropout(0.4), # Dropout for regularization

    # Dense Layer 2 (Output)
    layers.Dense(num_classes, activation="softmax") # Output layer with probabilities
])

# Print model summary
model.summary()

"""## **6. Compile and Train the Model**
This section configures the learning process and executes the model training. The compilation step defines the "rules" for improvement, while the training loop handles the actual learning through iterative passes.

*   `model.compile(...)` prepares the model for training by specifying the mathematical strategies for optimization and evaluation.
    *   `optimizer=keras.optimizers.Adam` advanced optimization algorithm that automatically adjusts the learning rate ($1 \times 10^{-3}$) for each parameter to speed up convergence.
    *  `loss="sparse_categorical_crossentropy"` the objective function used for multi-class classification where labels are provided as integers. The model strives to minimize this value.


*   `early_stop` callbacks function used to prevent overfitting and save computational time.

*   `model.fit(...)` feeds the processed data through the network.

"""

# 6 Compile and Train the Model

# Compile the model
model.compile(
    optimizer=keras.optimizers.Adam(learning_rate=1e-3),
    loss="sparse_categorical_crossentropy",
    metrics=["accuracy"]
)

# Early stopping
early_stop = keras.callbacks.EarlyStopping(
    monitor="val_loss",   # Watch validation loss
    patience=3,           # Stop if no improvement for 3 epochs
    restore_best_weights=True   # Roll back to the best validation loss weights
)

# Train the model
EPOCHS=30
history_initial = model.fit(
    ds_train,
    validation_data=ds_val,
    epochs=EPOCHS,
    callbacks=[early_stop]
)

"""## **7. Fine Tuning**
Fine-tuning is a process of refining the model's weights to achieve higher precision. This is typically done after initial training to "nudge" the model toward a better local minimum without destroying the patterns it has already learned.
*   `optimizer=keras.optimizers.Adam(learning_rate=1e-4)` re-compiles the model with a significantly lower learning rate ($1 \times 10^{-4}$). A smaller learning rate is crucial during fine-tuning to prevent large weight updates that could cause the model to forget previous learning
*   List item



"""

# 7 Fine Tuning

#Give lower learning rate
model.compile(
    optimizer=keras.optimizers.Adam(learning_rate=1e-4),
    loss="sparse_categorical_crossentropy",
    metrics=["accuracy"]
)

fine_tune_epochs = 20
history_fine = model.fit(
    ds_train,
    validation_data=ds_val,
    epochs=fine_tune_epochs
)

"""## **8. Plot Training Curves (Initial Training and after Fine Tuning)**
This section visualizes the model's learning progress by combining the metrics from both the initial training and fine-tuning phases into a single graph.

*   `acc / val_acc / loss / val_loss` these lines concatenate the lists of metrics from history_initial and history_fine. This allows for a continuous view of how the model improved across both training stages.

*   `plt.subplot(1,2,1)` creates the first chart in a side-by-side layout, focusing on Accuracy. This shows how well the model learned to correctly identify tissue types over time.
*   `plt.subplot(1,2,2)` creates the second chart focusing on Loss. This shows how the "error" or "penalty" decreased as the model's predictions became more confident and accurate.
*   `plt.axvline(...)` draws a vertical dashed line at the point where the initial training ended and fine-tuning began. This helps in identifying if the lower learning rate during fine-tuning provided a noticeable boost in performance.
*  `plt.tight_layout()` automatically adjusts the padding between the two subplots to ensure labels and titles do not overlap, making the final visualization clean and readable.




"""

# 8 Plot Training Curves (Initial Traning and after Fine Tuning)

# Accuracy and loss extraction
acc = history_initial.history["accuracy"] + history_fine.history["accuracy"]
val_acc = history_initial.history["val_accuracy"] + history_fine.history["val_accuracy"]

loss = history_initial.history["loss"] + history_fine.history["loss"]
